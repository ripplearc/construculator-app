/// Centralized icon size constants for the Construculator application.
/// Size names follow a t-shirt sizing convention aligned with the design system.
class CoreIconSizes {
  CoreIconSizes._();

  /// 14px — Extra-extra-small icons.
  static const double xxSmall = 14.0;

  /// 16px — Extra-small icons.
  static const double xSmall = 16.0;

  /// 20px — Small icons.
  static const double small = 20.0;

  /// 24px — Medium icons (default).
  static const double medium = 24.0;

  /// 32px — Large icons.
  static const double large = 32.0;

  /// 48px — Extra-large icons.
  static const double xLarge = 48.0;

  /// 64px — Extra-extra-large icons.
  static const double xxLarge = 64.0;
}
